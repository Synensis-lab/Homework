# GIT WORKFLOW FUNDAMENTALS
# 路        Working Directory
# Working directory is the local file that is being currently worked on.
# 路        Staging Area
# Staging Area is also on the local machine, the files are being prepared to go to the repository. Files are moved from
# working directory to the staging area.
# 路        Local Repo (head)
# The file from the staging area has been moved to the local repository. This means the local repository and local git repository are
# the same.
# 路        Remote repo (master)
#The remote repository is on git hub. It is where the files are stored remotely. Files are pushed from local to remote and
# pulled from remote to local.


